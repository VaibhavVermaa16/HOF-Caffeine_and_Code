const contract = new web3.eth.Contract(contractABI, contractAddress);

async function uploadImage() {
  const nftname = document.querySelector("#name").value;
  const desc = document.querySelector("#desc").value;

  const fileInput = document.getElementById("image");
  const file = fileInput.files[0];

  if (!file) {
    alert("Please select an image file.");
    return;
  }

  const reader = new FileReader();

  reader.onload = async function (event) {
    const imageURI = event.target.result;

    try {
      const accounts = await web3.eth.requestAccounts();
      const account = accounts[0];

      const result = await contract.methods
        .createNFT(nftname, desc, imageURI)
        .send({ from: account });
      const tokenId = result.events.Transfer.returnValues.tokenId;

      displayNFTDetails(tokenId, nftname, desc, imageURI);
    } catch (error) {
      console.error("Error creating NFT:", error);
      alert("Error creating NFT. Please try again.");
    }
  };

  reader.readAsDataURL(file);
}

// Function to display NFT details
async function displayNFTDetails(tokenId, nftname, desc, imageURI) {
  const nftContainer = document.getElementById("nftContainer");

  if (!nftContainer) {
    console.error("Error: nftContainer element not found.");
    return;
  }

  const nftDiv = document.createElement("div");
  nftDiv.classList.add("nftItem");

  const detailsHTML = `
            <h2>NFT Details</h2>
            <p>Name: ${nftname}</p>
            <p>Description: ${desc}</p>
            <img src="${imageURI}" alt="NFT Image">
        `;

  nftDiv.innerHTML = detailsHTML;
  nftContainer.appendChild(nftDiv);
}
